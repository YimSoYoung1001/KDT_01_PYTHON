# 숫자, 글자 데이터를 화면에 출력하기 (컴에게 시키고 싶은 일)
# 출력하기 => print(데이터)
# 숫자 데이터 출력 => 정수 int
print(2021)
print(1226)
print(33)

# 숫자 데이터 출력 => 실수 float
print(2.9)
print(9.9)
print(1.)

# 문자/글자/문자열 데이터 출력 => 글자 str
# '데이터'   "데이터"
print("홍길동")
print("지금은 겨울")
print('merry christmas')
print('2024.')   #여기서는 실수가 아니라 문자열임

